package com.himtech.googlechart;

public class ProductUsage {

	private String strProductName;
	private int availableLicense;
	private int usedLicense;
	
	public String getStrProductName() {
		return strProductName;
	}
	public void setStrProductName(String strProductName) {
		this.strProductName = strProductName;
	}
	public int getAvailableLicense() {
		return availableLicense;
	}
	public void setAvailableLicense(int availableLicense) {
		this.availableLicense = availableLicense;
	}
	public int getUsedLicense() {
		return usedLicense;
	}
	public void setUsedLicense(int usedLicense) {
		this.usedLicense = usedLicense;
	}
	
	
	
	
	
}
